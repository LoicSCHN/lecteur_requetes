package qengine.program;

public class Hexastore {
	private Index SPO;
	private Index POS;
	private Index OSP;
	private Index SOP;
	private Index OPS;
	private Index PSO;
	
	
	public Hexastore() {
		SPO = new Index();
		POS = new Index();
		OSP = new Index();
		SOP = new Index();
		OPS = new Index();
		PSO = new Index();
	}


	public Index getSPO() {
		return SPO;
	}


	public Index getPOS() {
		return POS;
	}

	public Index getOSP() {
		return OSP;
	}


	public Index getSOP() {
		return SOP;
	}


	public Index getOPS() {
		return OPS;
	}

	public Index getPSO() {
		return PSO;
	}



	
	
	
	
}
